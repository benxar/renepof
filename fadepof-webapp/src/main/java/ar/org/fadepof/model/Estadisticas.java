package ar.org.fadepof.model;

public class Estadisticas {

    private Integer epofReportadas;
    private Integer pacientesRegistrados;
    private Integer centrosRegistrados;

    public Integer getEpofReportadas() {
        return epofReportadas;
    }

    public void setEpofReportadas(Integer epofReportadas) {
        this.epofReportadas = epofReportadas;
    }

    public Integer getPacientesRegistrados() {
        return pacientesRegistrados;
    }

    public void setPacientesRegistrados(Integer pacientesRegistrados) {
        this.pacientesRegistrados = pacientesRegistrados;
    }

    public Integer getCentrosRegistrados() {
        return centrosRegistrados;
    }

    public void setCentrosRegistrados(Integer centrosRegistrados) {
        this.centrosRegistrados = centrosRegistrados;
    }
}
